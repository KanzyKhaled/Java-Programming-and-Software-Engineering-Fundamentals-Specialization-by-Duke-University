function changeColor(){
  var div1 = document.getElementById("div1"); 
  var div2 = document.getElementById("div2");
  
  div1.className = "blueback";
  div2.className = "redback";
  }

function changeText(){
  var div1 = document.getElementById("div1"); 
  var div2 = document.getElementById("div2");
  
  div1.innerHTML = "Erste";
  div2.innerHTML = "Zweite";
  }